package org.example.rentify.entity.enums;

/*
 * PaymentStatus is an enumeration that represents the various statuses of a payment
 * in the Rentify application. It is used to track the state of a payment throughout
 * its lifecycle, such as pending, completed, failed, or refunded.
 */
public enum PaymentStatus {
    PENDING,
    COMPLETED,
    FAILED,
    REFUNDED
}
