package org.example.rentify.entity.enums;

/*
* PropertyType is an enumeration that represents the different types of properties available in the Rentify application.
* It is used to categorize properties based on their type, such as apartments, houses, studios, villas, lofts, and penthouses.
 */
public enum PropertyType {
    APARTMENT,
    HOUSE,
    STUDIO,
    VILLA,
    LOFT,
    PENTHOUSE,
}
