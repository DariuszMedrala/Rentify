package org.example.rentify.entity.enums;

/**
 * PaymentMethod is an enumeration that represents the payment method in the Rentify application.
 * It can be used to track down what payment method was used, such as whether it was a credit card, paypal, bank transfer or cash.
 */
public enum PaymentMethod {
    CREDIT_CARD,
    PAYPAL,
    BANK_TRANSFER,
    CASH
}
